#include<stdio.h>
int main(void){

    // Fl0at - %f
    float fl = 234.90F;
    printf("Float - %f\n", fl);


    // Double - %lf
    double db = 46589.423;
    printf("Double - %lf\n", db);


    // Long Double - %Lf
    long double ld = 23409.340234;
    printf("Long Double - %Lf\n", ld);


    getch();
    return 0;
}
